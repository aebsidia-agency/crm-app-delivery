import * as yup from 'yup';

export const customerSchema = yup.object({
  name: yup
    .string()
    .required('Имя обязательно для заполнения')
    .min(2, 'Имя должно содержать минимум 2 символа')
    .max(50, 'Имя не должно превышать 50 символов'),
  
  phone: yup
    .string()
    .required('Телефон обязателен для заполнения')
    .matches(
      /^(\+375|375)?[\s-]?\(?[0-9]{2}\)?[\s-]?[0-9]{3}[\s-]?[0-9]{2}[\s-]?[0-9]{2}$/,
      'Введите корректный номер телефона (+375 XX XXX XX XX)'
    ),
  
  order: yup
    .string()
    .required('Заказ обязателен для заполнения')
    .min(10, 'Описание заказа должно содержать минимум 10 символов')
    .max(500, 'Описание заказа не должно превышать 500 символов'),
  
  status: yup
    .string()
    .oneOf(['Новый', 'Собирается', 'Доставлено'], 'Выберите корректный статус')
    .required('Статус обязателен для заполнения'),
  
  email: yup
    .string()
    .email('Введите корректный email адрес')
    .optional(),
  
  address: yup
    .string()
    .max(200, 'Адрес не должен превышать 200 символов')
    .optional(),
  
  notes: yup
    .string()
    .max(1000, 'Заметки не должны превышать 1000 символов')
    .optional(),
}); 